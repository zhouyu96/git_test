package com.etc.group7.entity;

public class User {
	//用户id
	private Integer user_id;
	//用户昵称
	private String user_name;
	//用户密码
	private String user_pwd;
	//用户性别
	private String user_gender;
	//用户真实姓名
	private String user_realname;
	//用户所在城市
	private String user_city;
	//用户邮箱
	private String user_email;
	//用户电话号码
	private String user_phonenum;
	//用户头像
	private String user_headpic;
	//用户信用积分
	private Integer user_point;
	//用户是否为骗子
	private boolean user_isfraud;
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer userId) {
		user_id = userId;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String userName) {
		user_name = userName;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String userPwd) {
		user_pwd = userPwd;
	}
	public String getUser_gender() {
		return user_gender;
	}
	public void setUser_gender(String userGender) {
		user_gender = userGender;
	}
	public String getUser_realname() {
		return user_realname;
	}
	public void setUser_realname(String userRealname) {
		user_realname = userRealname;
	}
	public String getUser_city() {
		return user_city;
	}
	public void setUser_city(String userCity) {
		user_city = userCity;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String userEmail) {
		user_email = userEmail;
	}
	public String getUser_phonenum() {
		return user_phonenum;
	}
	public void setUser_phonenum(String userPhonenum) {
		user_phonenum = userPhonenum;
	}
	public String getUser_headpic() {
		return user_headpic;
	}
	public void setUser_headpic(String userHeadpic) {
		user_headpic = userHeadpic;
	}
	public Integer getUser_point() {
		return user_point;
	}
	public void setUser_point(Integer userPoint) {
		user_point = userPoint;
	}
	public boolean isUser_isfraud() {
		return user_isfraud;
	}
	public void setUser_isfraud(boolean userIsfraud) {
		user_isfraud = userIsfraud;
	}
	
	

}
